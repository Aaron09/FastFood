package com.aarongreen.fastfood.DestinationActivityFiles;

import com.aarongreen.fastfood.DestinationActivityFiles.Destination;

import java.util.ArrayList;
import java.util.Arrays;

/**
 * Created by AaronGreen on 4/6/17.
 *
 * Used to house all of the Destination objects from the JSON
 */

public class DestinationCollection {

    private Destination[] results;
    private String status;

    /**
     *
     * @return the destinations from the JSON as an ArrayList
     */
    public ArrayList<Destination> getDestinationsAsArrayList() {
        return new ArrayList<>(Arrays.asList(results));
    }

    public Destination[] getDestinations() {
        return results;
    }

    public String getStatus() {
        return status;
    }
}
